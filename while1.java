/*4)Display numbers from 3 to 30 except number 24  using while loop.*/

class while1
 {
       public static void main(String args[])
      {
         int i=3;
         while(i<=30)
          {
            System.out.println(i);
              if(i==23)
                 ++i;
             i++;
          }
      }
     
 }
